// Keys that need innerHTML (contain HTML tags)
const htmlKeys = new Set([
  "prod.title", "faq.contact"
]);

let currentLang = localStorage.getItem('zzzlim-lang') || 'de';

function applyLanguage(lang) {
  if (!translations[lang]) return;
  currentLang = lang;
  localStorage.setItem('zzzlim-lang', lang);

  const dict = translations[lang];

  // Update all data-i18n elements
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (dict[key] === undefined) return;
    if (htmlKeys.has(key)) {
      el.innerHTML = dict[key];
    } else {
      el.textContent = dict[key];
    }
  });

  // Update language selector active state
  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.classList.toggle('active', btn.dataset.lang === lang);
  });

  // Update html lang attribute
  document.documentElement.lang = lang === 'tr' ? 'tr' : lang === 'en' ? 'en' : 'de';
}

function initI18n() {
  // Attach click handlers to language buttons
  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.addEventListener('click', () => {
      applyLanguage(btn.dataset.lang);
    });
  });

  // Apply saved language (if not default)
  if (currentLang !== 'de') {
    applyLanguage(currentLang);
  }
}

// Auto-init
if (document.readyState === 'loading') {
  document.addEventListener('DOMContentLoaded', initI18n);
} else {
  initI18n();
}
